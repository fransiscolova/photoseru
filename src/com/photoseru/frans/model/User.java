package com.photoseru.frans.model;

public class User {

	
	int iduser;
	String phone;
	String name;
	
	// constructors
	public User() {
		
	}

	public int getIduser() {
		return iduser;
	}

	public String getPhone() {
		return phone;
	}

	public String getName() {
		return name;
	}

	public void setIduser(int iduser) {
		this.iduser = iduser;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public void setName(String name) {
		this.name = name;
	}

	
	
	

}
